package sudoku;

public class Coordinates {

    private final int x;
    private final int y;

    public Coordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
//5:27 https://www.youtube.com/watch?v=qH9mWpYMtYU&t=34s
}
